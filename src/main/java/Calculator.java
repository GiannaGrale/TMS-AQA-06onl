import static java.lang.Math.sqrt;

public class Calculator {

    public int sum (int a, int b){
        return  a + b;
    }

    public  int div (int a, int b) {
        return  a/b;
    }

    public  double fraction (double c, double d) {
        return c/d;
    }

    public  double square (double f) {
        return sqrt(f);
    }
}
